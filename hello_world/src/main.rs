// Understanding Match in Rust

fn main() {
    let my_num = 7;

    match my_num {
        1 => println!("One"),
        2=> println!("Two"),
        3 => println!("Three"),
        4 => println!("Four"),
        5 => println!("Five"),
        6 => println!("Six"),
        7 => println!("Seven"),
        8 => println!("Eight"),
        9 => println!("Nine"),
        10 => println!("Ten"),
        _ => println!("Something else")
    }
}
